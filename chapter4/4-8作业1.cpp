#include <iostream>
using namespace std;
int main()
{
    //q 二进制01110001
    //求反运算符会将char型升到int型
    //        q 00000000 00000000 00000000 01110001
    //       ~q 11111111 11111111 11111111 10001110
    //左移6位   11111111 11111111 11100011 10000000
    //int型 -1  11111111 11111111 11111111 11111111

    //所以      11111111 11111111 11100011 10000000
    //        + 00000000 00000000 00011100 01111111（7295） = -1
    //所以      11111111 11111111 11100011 10000000 = -7296
    char q = 113;
    cout<<(~q <<6)<<endl;  //-7296




    return 0;
}
